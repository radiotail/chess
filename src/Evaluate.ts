const MVV_LVA = [
    11, 2, 2, 4, 10, 5, 2, 0,
    11, 2, 2, 4, 10, 5, 2, 0,
    11, 2, 2, 4, 10, 5, 2, 0,
];

// 求MVV/LVA值 
export function mvvLva(situation: Situation, dstSq: number, srcSq: number) {
    return MVV_LVA[situation.getPiece(dstSq)] - MVV_LVA[situation.getPiece(srcSq)];
}

export function mvvLvaByMove(situation: Situation, move: number) {
    return MVV_LVA[situation.toDstSq(move)] - MVV_LVA[situation.toSrcSq(move)];
}

export class Evaluate {
    situation: Situation;
    search: Search;

    constructor(situation: Situation, search: Search) {
        this.situation = situation;
        this.search = search;
    }
    
    mateValue(): number {
        return this.situation.depth - MATE_VALUE;
    }

    banValue(): number {
        return this.situation.depth - BAN_VALUE;
    }

    drawValue(): number {
        return (this.situation.depth & 1) == 0 ? -DRAW_VALUE : DRAW_VALUE;
    }

    nullOkay(): boolean {
        return this.situation.controllerValue() > NULL_OKAY_MARGIN;
    }

    nullSafe(): boolean {
        return this.situation.controllerValue() > NULL_SAFE_MARGIN;
    }

    genEvaluate(): number {
        let value = this.situation.controllerValueDiff() + FIRST_VALUE;
        return value == this.drawValue() ? value - 1 : value;
    }

    repValue(repValue): number {
        let value = ((repValue & 2) == 0 ? 0 : this.banValue()) + ((repValue & 4) == 0 ? 0 : -this.banValue());
        return value == 0 ? this.drawValue() : value;
    }
}

